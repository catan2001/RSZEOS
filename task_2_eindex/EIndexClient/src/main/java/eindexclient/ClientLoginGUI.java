    @Override
    public void handleServerMessage(String message) {
        java.awt.EventQueue.invokeLater(() -> {
            if (message.startsWith("LOGIN_OK_ADMIN")) {
                JOptionPane.showMessageDialog(this, "Login successful as Admin!");
                app.switchToAdmin();       // switch GUI in main client
                this.dispose();             // close login window
            } else if (message.startsWith("LOGIN_OK_STUDENT")) {
                JOptionPane.showMessageDialog(this, "Login successful as Student!");
                app.switchToStudent(textFieldBoxUsername.getText().trim());      // switch GUI in main client
                this.dispose();
            } else if (message.startsWith("LOGIN_FAIL")) {
                JOptionPane.showMessageDialog(this, "Login failed: " + message.split(" ", 2)[1]);
            }
        });
    }

