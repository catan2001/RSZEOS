    private void buttonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonLoginActionPerformed
        String username = textFieldBoxUsername.getText().trim();
        String password = new String(textFieldBoxPassword.getPassword());
        String role = comboBoxAdminRole.getSelectedItem().toString().toLowerCase();

        // Simple validation
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter both username and password!");
            return;
        }

        // Send login request to server
        writer.println("LOGIN:" + role + ":" + username + ":" + password);
    }//GEN-LAST:event_buttonLoginActionPerformed

